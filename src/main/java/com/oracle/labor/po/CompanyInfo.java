package com.oracle.labor.po;

public class CompanyInfo {
	
	private String zpbh;
	
	private String bioName;
	
	private String bioBuaAddress;
	
	private String lxrsj;

	public String getZpbh() {
		return zpbh;
	}

	public void setZpbh(String zpbh) {
		this.zpbh = zpbh;
	}

	public String getBioName() {
		return bioName;
	}

	public void setBioName(String bioName) {
		this.bioName = bioName;
	}

	public String getBioBuaAddress() {
		return bioBuaAddress;
	}

	public void setBioBuaAddress(String bioBuaAddress) {
		this.bioBuaAddress = bioBuaAddress;
	}

	public String getLxrsj() {
		return lxrsj;
	}

	public void setLxrsj(String lxrsj) {
		this.lxrsj = lxrsj;
	}
	
}
